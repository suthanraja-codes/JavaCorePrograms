public class bitwise {
    public static void main (String[] args)
    {
        int a=25,b=45;
        System.out.println("Bitwise OR :"+(a & b));
        System.out.println("Bitwise OR :"+(a | b));
        System.out.println("Bitwise OR :"+(a ^ b));
        System.out.println("Bitwise OR :"+(~a));
        System.out.println("Bitwise OR :"+(a<<b));
        System.out.println("Bitwise OR :"+(a>>b));
        System.out.println("Bitwise OR :"+(a>>>b));
    }
}
